# # Whitespace Formatting

# In[1]:


for i in [2]:
    print(i)              # first line in "for i" block
    for j in [15]:
        print(j)          # first line in "for j" block
        print(i + j)      # last line in "for j" block
    print(i + 1)          # last line in "for i" block
print('done looping')


# In[2]:


for i in [2, 5]:
    print(i)
    for j in [15, 28]:
        print(j)
        print(i + j)
    print(i + 1)
print('done looping')


